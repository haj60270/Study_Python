# --- no.01 四則演算
test_int = 100
print(test_int + 10)
print(test_int - 10)
print(test_int * 10)
print(test_int / 10)

# --- no.02 数値へ変換
test_str = '123'
print(int(test_str))

# --- no.03 浮動小数点
test_str = '987.65'
print(float(test_str))

test_str = '.43'
print(float(test_str))

# --- no.04 複素数
# * 複素数とは実数と虚数を組み合わせたもの *
test_complex = 100 + 5j
print(test_complex)
print(test_complex.real)
print(test_complex.imag)

